package Java_003_DataTypes;

public class Lab018_PrimitiveDataType {
    // Primitive Data Type provided by the Java guys.
    // there are 8 primitive types
    // Data Type	Size	    Example 	                Used For
    // byte	        1 byte	    byte age = 25;	            Small numbers (-128 to 127)
    // short	    2 bytes	    short salary = 30000;	    Medium numbers
    // int	        4 bytes	    int empId = 1024;	        Most common whole numbers
    // long	        8 bytes	    long phone = 9999999999L;	Very large numbers
    // float     	4 bytes	    float price = 99.99f;	    Decimal numbers
    // double	    8 bytes	    double rating = 4.5;	    More precise decimal values
    // char	        2 bytes	    char grade = 'A';	        Single character
    // boolean	    1 bit	    boolean isLogin = true;	    True/False

}
