package Java_004_Operators.ex4_Increment_Decrement.ex1_Increment;

public class Lab037_Prefix {
    public static void main(String[] args) {
        // Increment Operator ( ++ )
        // Value is Incremented first then stored in the result.
        // 1. Prefix Increment:  ++variable
        int a = 10;
        int b = ++a;
        System.out.println(b);


        // Expression and Result Table
        // Line Number   |  a  |   Result
        //   8           | 10  | NA
        //   9           |11   | 11


    }
}
