package Java_008_Loops.Ex_01_ForLoop;

public class Lab073_Definition {
    public static void main(String[] args) {
       // Loops:
        // A loop is used to execute a block of code repeatedly until a condition becomes false.
        //Used for repetitive tasks like:
        //✔ executing test cases
        //✔ reading data from arrays
        //✔ retry logic
        //✔ iterating web elements in Selenium.


        // Used when you know the number of iterations.
        // For Loop is divided into 3 part


        // 1. Initialization, 2. Condition, Updation (Increment / Decrement)

        for (int i = 1; i<= 10; i++){
            // block of code that we can run multiple.
            System.out.println(i);
        }
    }
}
